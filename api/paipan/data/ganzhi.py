SHENG_STAGES = ["长生", "沐浴", "冠带", "临官", "帝旺", "衰", "病", "死", "墓", "绝", "胎", "养"]


def get_changsheng_stage(gan: str, zhi: str) -> str:
    """获取某天干在某地支的十二长生状态"""
    changsheng_start = CHANGSHENG[gan]
    start_idx = DI_ZHI.index(changsheng_start)
    target_idx = DI_ZHI.index(zhi)
    offset = (target_idx - start_idx) % 12
    return SHENG_STAGES[offset]


