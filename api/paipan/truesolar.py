def correct_time(city: str, hour: int, minute: int = 0) -> tuple:
    """
    校正真太阳时。

    Args:
        city: 城市名
        hour: 北京时间小时 (0-23)
        minute: 北京时间分钟 (0-59)

    Returns:
        (校正后小时, 校正后分钟, 时差分钟数)
    """
    coords = CITY_COORDS.get(city)
    if not coords:
        # 模糊匹配
        for name in CITY_COORDS:
            if city in name or name in city:
                coords = CITY_COORDS[name]
                break

    if not coords:
        # 未找到，按北京处理
        coords = (116.40, 39.90)

    lng, _ = coords
    # 时差 = (当地经度 - 120) × 4 分钟
    offset = round((lng - 120) * 4)
    total_minutes = hour * 60 + minute + offset
    total_minutes = total_minutes % (24 * 60)  # 限制在一天内
    if total_minutes < 0:
        total_minutes += 24 * 60

    corrected_hour = total_minutes // 60
    corrected_minute = total_minutes % 60

    return corrected_hour, corrected_minute, offset


def get_city_list() -> list:
    """获取支持的城市列表"""
    return sorted(CITY_COORDS.keys())
